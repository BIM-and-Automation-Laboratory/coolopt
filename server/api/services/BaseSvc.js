const EventEmitter = require("events");

class BaseSvc extends EventEmitter {
  /// //////////////////////////////////////////////////////////////
  //
  //
  /// //////////////////////////////////////////////////////////////
  constructor(config = {}) {
    super();

    this._config = config;
  }

  /// //////////////////////////////////////////////////////////////
  //
  //
  /// //////////////////////////////////////////////////////////////
  name() {
    return this._name;
  }

  /// //////////////////////////////////////////////////////////////
  //
  //
  /// //////////////////////////////////////////////////////////////
  config() {
    return this._config;
  }
}

module.exports = BaseSvc
